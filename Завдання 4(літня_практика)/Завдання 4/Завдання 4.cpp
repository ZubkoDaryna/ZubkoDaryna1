#include <iostream>
#include <vector>
#include <algorithm>
using namespace std;

int main()
{
    vector<double> numbers;
    double number;
    cout << "Input numbers(print '0' to stop:)" << endl;
    while (true) {
        cin >> number;
        if (number == 0.0) {
            break;
        }
        numbers.push_back(number);
    }
    vector<double>::iterator ptin_iterator = numbers.begin();
    vector<double>::iterator ptout_iterator = numbers.end();

    cout << "Your numbers: ";
    copy(ptin_iterator, ptout_iterator, ostream_iterator<double>(cout, " ")); 
    cout << endl;
	return 0;
}

